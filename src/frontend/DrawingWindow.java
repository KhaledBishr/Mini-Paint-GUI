    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int index=jComboBox1.getSelectedIndex();
        if (index > 0) {
            Color c = JColorChooser.showDialog(this, "Choose color", Color.yellow);
            Shape s= drawingEngine1.getShapes()[index - 1];
            s.setFillColor(c);
            drawingEngine1.refresh(drawingEngine1.getGraphics()); 
        } else {
        JOptionPane.showMessageDialog(this, "Please select a shape to fill color it.");
    }    }//GEN-LAST:event_jButton2ActionPerformed

