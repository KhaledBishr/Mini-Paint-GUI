    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
        double x = Double.parseDouble(JOptionPane.showInputDialog("Enter x"));
        double y = Double.parseDouble(JOptionPane.showInputDialog("Enter y"));
        double radius =Double.parseDouble(JOptionPane.showInputDialog("Enter Raduis"));
    
        Point position = new Point();
        position.setLocation(x, y);
        
        Circle r = new Circle(position, radius, Color.black);
        drawingEngine1.addShape(r);
        jComboBox1.addItem("Circle " + drawingEngine1.shapes.size());
         drawingEngine1.refresh(drawingEngine1.getGraphics());
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this,"Enter Value");
        }
            
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 int Index = jComboBox1.getSelectedIndex();

    if (Index > 0) {
       
         drawingEngine1.removeShape(drawingEngine1.getShapes()[Index - 1]);

        jComboBox1.removeItemAt(Index);

        drawingEngine1.refresh(drawingEngine1.getGraphics()); 
    } else {
        JOptionPane.showMessageDialog(this, "Please select a shape to delete.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

